class Karyawan {
	protected String nama;
	protected String nik;

    public void setNama(String nama) {
    	this.nama = nama;
    }
    
    public void setNik(String nik) {
    	this.nik = nik;
    }
    
    public String getNama() {
    	return nama;
    }
    
    public String getNik() {
    	return nik;
    }
    
    
}